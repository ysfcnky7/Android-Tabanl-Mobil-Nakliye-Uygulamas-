public class Anlasma {
    private String ID, tarih, fiyat, kullanici_ID, ilan_ID, state;

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getTarih() {
        return tarih;
    }

    public void setTarih(String tarih) {
        this.tarih = tarih;
    }

    public String getFiyat() {
        return fiyat;
    }

    public void setFiyat(String fiyat) {
        this.fiyat = fiyat;
    }

    public String getKullanici_ID() {
        return kullanici_ID;
    }

    public void setKullanici_ID(String kullanici_ID) {
        this.kullanici_ID = kullanici_ID;
    }

    public String getIlan_ID() {
        return ilan_ID;
    }

    public void setIlan_ID(String ilan_ID) {
        this.ilan_ID = ilan_ID;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }
}
